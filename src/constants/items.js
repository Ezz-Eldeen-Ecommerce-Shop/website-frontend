function enhanceItems(items) {
  const today = new Date().toISOString().split('T')[0]; // Get current date in YYYY-MM-DD format
  return items.map(item => {
    const discountPercentage = Math.random() > 0.5 ? Math.floor(Math.random() * 50) + 1 : 0; // 50% chance of having a discount
    return {
      ...item,
      dateOfAdding: today, // Adding current date
      sold: Math.floor(Math.random() * 101), // Random number of sold items between 0-100
      discount: discountPercentage > 0 ? `${discountPercentage}%` : null, // Null if no discount
    };
  });
}

